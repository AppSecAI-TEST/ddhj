package cn.com.ddhj.result.pollution;

public class PdData {
	private int EntCode;
	private String EntName;
	private int GasStnums;
	private double Lat;
	private double Lon;
	private int WaterStnums;
	
	
	public int getEntCode() {
		return EntCode;
	}
	public void setEntCode(int entCode) {
		EntCode = entCode;
	}
	public String getEntName() {
		return EntName;
	}
	public void setEntName(String entName) {
		EntName = entName;
	}
	public int getGasStnums() {
		return GasStnums;
	}
	public void setGasStnums(int gasStnums) {
		GasStnums = gasStnums;
	}
	public double getLat() {
		return Lat;
	}
	public void setLat(double lat) {
		Lat = lat;
	}
	public double getLon() {
		return Lon;
	}
	public void setLon(double lon) {
		Lon = lon;
	}
	public int getWaterStnums() {
		return WaterStnums;
	}
	public void setWaterStnums(int waterStnums) {
		WaterStnums = waterStnums;
	}
	
	
}
