package cn.com.ddhj.result.llConvertion;

public class CResult {
	private String lng;
	private String lat;
	private double off_lng;
	private double off_lat;
	private String type;
	
	
	public String getLng() {
		return lng;
	}
	public void setLng(String lng) {
		this.lng = lng;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public double getOff_lng() {
		return off_lng;
	}
	public void setOff_lng(double off_lng) {
		this.off_lng = off_lng;
	}
	public double getOff_lat() {
		return off_lat;
	}
	public void setOff_lat(double off_lat) {
		this.off_lat = off_lat;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
}
